$(function() {
	$('.exp-col01')
		.unbind('click')
		.click(function () {
			var index = $('.exp-col01').index($(this));
			$('#switchdiv01').slideToggle('slow');
			$('.exp-col01').not(':eq(' + index + ')').show();
			$(this).hide();
		});
});

