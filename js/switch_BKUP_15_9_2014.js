$(function() {
	$('.exp-col027')
		.on('click',function(){

			
			if($('.topplus').css('display') !="block"){
					$('#left_container .nw-content').slideUp();
				$('.topplus').show();
				$('.topminus').hide();
				
				$('.minus').hide();
				$('.plus').show();
				
			} else {
				$('#left_container .nw-content').slideDown();
				$('.topplus').hide();
				$('.topminus').show();
				
				$('.minus').show();
				$('.plus').hide();	
			}
			
			
			
		});
});

